void KeyPad_Init(void)
{
	DIO_WriteChannel(KeyPad_Col0,PIN_HIGH);
	DIO_WriteChannel(KeyPad_Col1,PIN_HIGH);
	DIO_WriteChannel(KeyPad_Col2,PIN_HIGH);
	/*DIO_WriteChannel(KeyPad_Col3,PIN_HIGH);*/
}
/******************************************************************************
* \Syntax          : Std_ReturnType FunctionName(AnyType parameterName)
* \Description     : Describe this service
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
uint8 KeyPad_GetValue(void)
{
	uint8 row_cnt=Row_Init,col_cnt=0;
	uint8 button_val=0,temp=0;
	for(row_cnt=Row_Init;row_cnt<=Row_Final;row_cnt++)
	{
		DIO_WriteChannel(row_cnt,PIN_LOW);
		for(col_cnt=Col_Init;col_cnt<=Col_Final;col_cnt++)
		{
			DIO_ReadChannel(col_cnt,&temp);
			if(temp==0)
			{
				button_val=KeyPad_value[row_cnt-Row_Init][col_cnt-Col_Init];
				while(temp==0)
				{
					DIO_ReadChannel(col_cnt,&temp);
				}
				_delay_ms(10);
			}
		}
		DIO_WriteChannel(row_cnt,PIN_HIGH);
	}
	return button_val;
}
